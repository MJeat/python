# Tree data structure: a data structure that is based on a hierarchical structure
# Each node in the tree has two or more children nodes

# It is a non-linear data structure where nodes are organized in a hierarchy (like a family tree)

# Where to use: File explorer, databases, DNS (domanain name server), HTML DOM, etc.


# The top of the tree is called the root node (parent node)
# And branch nodes are in the middle (both)
# The bottom of the tree is called the leaf node (child node)

# nodes that have the same parents are siblings

# Size of a tree is the number of nodes in the tree
# Height of the tree is the number of edges between the root node and the farthest leaf node
# Depth of the tree is the number of edges below the root node.