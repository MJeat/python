# For loops: use to execute a code with limited amount of time.
#For loop: limited of time
#While Loop: unlimited of time to execute


for i in range(200):
    print(i)

#i here means index

for p in range(50, 200+1):
    print(p)

for pi in range(50, 200+1, 2): #Similar to (start, stop, step)
    print(pi)

